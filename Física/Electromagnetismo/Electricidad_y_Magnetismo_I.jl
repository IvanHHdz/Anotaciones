md"""
## Conversión entre coordenadas

Esto resulta particularmente útil para ciertos casos en los que se desean resolver ciertos problemas. Esencialmente se trata de una sustitución de variable un poco más profunda. Esto ya que no solo se trata de cambiar las variables, sino también las direcciones; brindando así una forma de seguir trabajando en con vectores definidos un poco distinto a como se suelen definir. Es útil.

No entraré en detalle de cómo se demuestran estas igualdades que sirven luego para realizar los cambios de coordenadas. Ya que es una análisis principalmente geométrico. Bastante interesante, sencillo de lograr pero innecesario de explicar aquí por los fines que aquí se buscan. Pues más allá de entender el significado geométrico de cada magnitud, no es necesario entrar más a detalle.

### Coordenadas Cilíndricas

En estas coordenadas se considera un círculo en el plano $xy$. Con eso para un punto en el espacio, se considera primero una distancia al eje $\hat z$ llamada $\rho$, siendo esta la primera dirección $\hat\rho$. Para ubicar este en el plano $xy$ se necesita una segunda coordenada, $\phi$, con su dirección $\hat\phi$. Esto es igual a como se hace en coordenadas polares en $\mathbb R ^2$. La tercera coordenada es simplemente es $z$ en la dirección de $\hat z$; exactamente igual que la $z$ cartesiana.

Entonces, como sabemos, un punto atado a una circunferencia funciona como un círculo unitario. Entonces:

```math
\begin{cases}
	\rho &= \sqrt{x^2 + y^2}\\
	\phi &= \arctan{\frac yx}\\
	z &= z
\end{cases}
```

Y de regreso:

```math
\begin{cases}
	x &= \rho \cos \phi\\
	y &= \rho \sin \phi\\
	z &= z
\end{cases}
```

De igual forma se deben hacer los cambios para las bases:

```math
\begin{cases}
	\hat\rho &= \cos\phi\hat x + \sin\phi\hat y\\
	\hat\phi &= -\sin\phi\hat x + \cos\phi\hat y\\
	\hat z &= \hat z
\end{cases}
```

Y también de regreso:

```math
\begin{cases}
	\hat x &= \cos\phi\hat\rho - \sin\phi\hat\phi\\
	\hat y &= \sin\phi\hat\rho + \cos\phi\hat\phi\\
	\hat z &= \hat z
\end{cases}
```

Si le parece extraño que en ambas conversiones de las bases aparezca siempre $\phi$ no se preocupe, pronto se dará cuenta que es útil. De igual forma, es posible demostrar que esto debe ser así por medio de un análisis geométrico. Omitiremos esa parte.

### Coordenadas Esféricas

En esféricas se tiene una esfera. Para ejemplificar podemos usar al propio planeta tierra y su sistema coordenado. En la tierra, nos ubicamos por la altitud, que mide la altura con referencia al nivel del mar; la latitud, que mide los grados en relación al Ecuador; y la longitud, que mide los grados en relación al meridiano de Greenwich. De manera similar, en coordenadas esféricas tenemos un análogo a la altitud, pero medido a partir del origen, $r$ en dirección de $\hat r$; un análogo a la latitud pero con relación al eje $z$, llamado $\theta$, en dirección de $\hat\theta$; y tenemos un análogo a la longitud, pero con relación al eje $x$, llamado $\varphi$ en dirección de $\hat\varphi$. E igual que en la tierra donde la longitud y latitud se miden angularmente, $\theta$ y $\varphi$ son ángulos.

Así pues, llegamos a las conversiones:

```math
\begin{cases}
	x &= r \sin\theta\cos\varphi\\
	y &= r \sin\theta\sin\varphi\\
	z &= r \cos\theta
\end{cases}
```

Y de regreso:

```math
\begin{cases}
	r &= \sqrt{x^2 + y^2 + z^2}\\
	\varphi &= \arctan \frac yx\\
	\theta &= \arccos \frac z{\sqrt{x^2 + y^2 + z^2}}
\end{cases}
```

Cabe destacar que, si bien yo uso $\varphi$ para  denotar esta coordenada, sigue siendo igual a $\phi$ de coordenadas cilíndricas.

En cuanto a las bases:

```math
\begin{cases}
	\hat r &= \sin\theta\cos\varphi\hat x + \sin\theta\sin\varphi\hat y + \cos\theta\hat z\\
	\hat \theta &= \cos\theta\cos\varphi\hat x + \cos\theta\sin\varphi\hat y - \sin\theta\hat z\\
	\hat\varphi &= -\sin\varphi\hat x + \cos\varphi\hat y
\end{cases}
```

Y su regreso:

```math
\begin{cases}
	\hat x &= \sin\theta\cos\varphi\hat r + \cos\theta\cos\varphi\hat\theta -\sin\varphi\hat\varphi\\
	\hat y &= \sin\theta\sin\varphi\hat r + \cos\theta\sin\varphi\hat\theta +\cos\varphi\hat\varphi\\
	\hat z &= \cos\theta\hat r - \sin\theta\hat\theta
\end{cases}
```

Igual aquí, he decidido usar $\hat\varphi$ en lugar del de cilíndricas, $\hat\phi$, solo para diferenciar. Aún así, son lo mismo ambas.

