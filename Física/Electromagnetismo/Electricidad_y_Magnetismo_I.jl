md"""
### Rotacional

Otra operación vectorial importante es el rotacional. De igual forma que con la divergencia, el rotacional se aplica sobre un campo vectorial, solo que retornando un vector. Esta mide la tendencia de un flujo de rotar alrededor de un punto. El rotacional de un campo vectorial $\vec F$ es denotado por $\text{curl}\vec F$ o $\vec\nabla\times\vec F$.

Matemáticamente se define como:

```math
\text{curl}\vec F = \lim_{A \to 0} \frac{1}{\lVert A \rVert} \oint_{C(A)} \vec F d\vec \ell
```

Y de igual forma que con la divergencia, por su complejidad se suele calcular haciendo uso de la siguiente igualdad:

```math
\vec\nabla\times\vec F = \begin{vmatrix}
		\hat x & \hat y & \hat z \\
		\frac{\partial}{\partial x} & \frac{\partial}{\partial y} & \frac{\partial}{\partial z}\\
		F_x & F_y & F_z
	\end{vmatrix} = \left(\frac{\partial F_z}{\partial y} - \frac{\partial F_y}{\partial z}\right) \hat x - \left(\frac{\partial F_z}{\partial x} - \frac{\partial F_x}{\partial z}\right) \hat y + \left(\frac{\partial F_y}{\partial x} - \frac{\partial F_x}{\partial y}\right) \hat z
```

Si la notación dentro de la determinante de la matriz parece rara, es porque lo es. Se toma como si $\vec\nabla$ representara un vector de los operadores de derivadas parciales. Esto es absoluta y completamente incorrecto y **no** es apropiada, pues esto no es así. Los operadores no pueden ser vectores, ni estar dentro de vectores. Eso no tiene ningún sentido y esta interpretación (dada a la divergencia como producto escalar y al rotacional como producto vectorial) son **incorrectas**, incoherentes y carentes de todo sentido matemático. $\vec\nabla$ no representa nada, pues solo es parte de la notación para denotar el gradiente, y se suele asociar a los operadores de divergencia y rotacional.

Ahora bien, esta interpretación resulta útil para recordarlas, así que se dejará pasar. A pesar de ser completamente **errónea**.
"""

# ╔═╡ c8976b4e-ec9f-4368-aa87-e7cb1ebbbaaf
begin
	function rotacional(F)
		Fx = F[1]
		Fy = F[2]
		Fz = F[3]
		∂x = Differential(x)
		∂y = Differential(y)
		∂z = Differential(z)
