# Ley de Coulomb

Esta ley nos indica la fuerza entre dos cargas. Como tal, esta ley matemáticamente es:

```math
\vec F_e = \frac 1{4\pi \varepsilon_0} \frac{q\,q'}{r^2} \hat r
```

Donde $q$ es la carga de campo y $q'$ la carga fuente. Aquí también $r$ es la distancia entre las dos cargas y $\hat r$ es el vector unitario que apunta de la carga fuente a la carga de campo.

Sin embargo, no siempre es tan sencillo medir la distancia $r$ ni conocer el vector director $\hat r$. Por lo que se utiliza un análisis vectorial.

De esta forma, se definen los vectores directores $\vec r$ y $\vec r'$ como los vectores que apuntan a la carga de campo y la carga fuente respectivamente. Entonces, utilizando las propiedades de la suma de vectores:
```math
\vec r' + \vec R = \vec r
```

De aquí se obtiene que $\vec R$ será un vector que va de la carga fuente a la carga de campo. Despejando se obtiene que:
```math
\vec R = \vec r - \vec r'
```

Y claro, la distancia entre las cargas será el módulo de $\vec R$, o sea $\lVert \vec R\rVert$. Sin embargo, considere que el vector que aparece originalmente es un vector unitario en dirección de la carga fuente a la de campo, pero $\vec R$ no es, necesariamente, unitario. Por lo que lo volvemos unitario al dividirlo por su módulo, es decir:
```math
\hat R = \frac{\vec R}{\lVert \vec R \rVert}
```

Así, la ley de Coulomb nos quedaría como:
```math
\vec F_e = \frac 1{4\pi \varepsilon_0} \frac{q\,q'}{\lVert \vec R \rVert^3} \hat R
```

Claro que, frecuentemente, ocurrirá que no tenemos dos cargas puntuales, sino conjuntos de cargas puntuales o directamente distribuciones de carga. En caso de varias cargas puntuales la fuerza sería la suma de todas las contribuciones:
```math
\vec F_e = \frac 1{4\pi \varepsilon_0} \sum_i\sum_j\frac{q_i\,q'_j}{\lVert \vec R_{ij} \rVert^3} \hat R_{ij}
```
Y en caso de distribuciones sería la integral:
```math
\vec F_e = \frac 1{4\pi \varepsilon_0} \iint\frac{dq\,dq'}{\lVert \vec R \rVert^3} \hat R
```

Recordando que las distribuciones pueden ser lineales, superficiales o volumétricas. Representadas por $\lambda$, $\sigma$ y $\rho$ respectivamente. Teniendo que:
```math
\begin{cases}
	dq =& \lambda \, d\ell\\
	dq =& \sigma \, da\\
	dq =& \rho \, d\tau\\
\end{cases}
```

