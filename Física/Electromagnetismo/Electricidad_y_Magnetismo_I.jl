		curl_y = expand_derivatives(∂z(Fx) - ∂x(Fz))
		curl_z = expand_derivatives(∂x(Fy) - ∂y(Fx))
		curl_F = [curl_x, curl_y, curl_z]
		curl_F
	end
	
