md"""

### Divergencia

La divergencia de un campo vectorial ``\vec F`` denotada por $\text{div}\vec F$ o $\vec\nabla\cdot\vec F$, se define formalmente como la medida de flujo de un punto en un campo vectorial. Es decir, la relación de flujo (dado por un campo vectorial) que atravieza una superficie que encierra un punto dado, cuando dicha superficie es infinitamente pequeña.

Matemáticamente se expresa como:

```math
\text{div}\vec F = \lim_{V \to 0} \frac{1}{\lVert V \rVert} \oint_{S(V)} \vec F \cdot d\vec S
```

Pero esto es complicado de calcular directamente, y curiosamente es igual a calcular:

```math
	\vec \nabla \cdot \vec F = \frac{\partial F_x}{\partial x} + \frac{\partial F_y}{\partial y} + \frac{\partial F_z}{\partial z}
```

Por lo que se opta por usar la segunda definición. Es importante resaltar que ambas definciones son equivalentes.
"""

# ╔═╡ 0ed74c81-56f1-412e-b74c-d332453afe40
begin
	function divergencia(F)
		Dx = Differential(x)
		Dy = Differential(y)
		Dz = Differential(z)
		div_F = expand_derivatives(Dx(F[1]) + Dy(F[2]) + Dz(F[3]))
		div_F
	end
	
