### Teorema de la Divergencia y Teorema de Stokes

Estos dos teoremas serán relevantes más adelante para probar ciertas identidades o igualdades. O simplificar ciertas partes.

#### Teorema de la divergencia

Sea $\vec A$ un campo vectorial y $V$ un volumen. Todo esto en $\mathbb R^3$. Entonces, se dice que la suma de todo el flujo que emerge dentro de un volumen es igual al flujo que atraviesa la superficie de dicho volumen. Matemáticamente se expresa como:

```math
\iiint_V(\vec\nabla\cdot\vec A)\, dV = \iint_{S(V)} \vec A \cdot d\vec a
```

Donde $S(V)$ es el área de la superficie de $V$.
"""

# ╔═╡ 118e3054-3b96-43d0-8b32-b0c4a2d8240d
function integracion(f, v, min, max)
	primitive = integrate(f, v)
	substitute(primitive, Dict(v => max)) - substitute(primitive, Dict(v => min))
end;

# ╔═╡ 01e74910-120c-4efd-ac78-5824f2400a25
function iiintegracion(F, (u, a, b), (v, c, d), (w, e, f))
	i1 = integracion(F, u, a, b)
	i2 = integracion(i1, v, c, d)
	i3 = integracion(i2, w, e, f)
	i3
end;

# ╔═╡ ee44d619-3814-4a6f-a619-b16835ec9328
function iintegracion(F, (u, a, b), (v, c, d))
	i1 = integracion(F, u, a, b)
	i2 = integracion(i1, v, c, d)
	i2
end;

